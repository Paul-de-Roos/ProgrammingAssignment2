## this function makes a list of 4 functions which can be assigned to a vector and subsequently
## 'called' by the cacheSolve function
makeCacheMatrix <- function(x = matrix()) {
  inv <- NULL
  set <- function(y) {
    x <<- y
    inv <<- NULL
  }
  get <- function() x
  setinv <- function(solve) inv <<- solve
  getinv <- function() inv
  list(set = set, get = get,
       setinv = setinv,
       getinv = getinv)
}

## this funtion checks if its argument is stored with the appropiate functions assigned to it.
## If so, it will retrieve the inverse matrix from cache. If not, it will calculate the inverse matrix.
cacheSolve <- function(x, ...) {
  inv <- x$getinv()
  if(!is.null(inv)) {
    message("getting cached data")
    return(inv)
  }
  data <- x$get()
  inv <- solve(data, ...)
  x$setinv(inv)
  inv
}